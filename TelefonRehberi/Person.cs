namespace TelefonRehberi
{
    public class Person
    {

        public string firstName = "";
        public string lastName = "";
        public string phone = "";

        public Person()
        {

        }

        public Person(string firstName, string lastName, string phone)
        {
            this.firstName = firstName;
            this.lastName = lastName;
            this.phone = phone;
        }


    }
}
